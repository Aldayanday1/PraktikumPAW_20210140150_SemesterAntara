package com.PraktikumPAW;

public class UnaryOperator {
    public static void main(String[] args) {
        int d = +100;

        d++; //meningkatkan nilai d = 1
        System.out.println(d);

        d--; //mengurangi nilai d = 1
        System.out.println(d);

        System.out.println(!true);
    }
}
